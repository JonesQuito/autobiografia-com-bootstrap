
function resizeDown(){
  divPrincipal = document.getElementById("biografia");
  divPrincipal.style.backgroundColor = 'rgba(0,0,0,1)';
  divPrincipal.style.margin = '10px 5%';
  divPrincipal.style.transition = '0.5s';
}

function resizeUp(){
  divPrincipal = document.getElementById("biografia");
  divPrincipal.style.backgroundColor = 'rgba(0,0,0,.8';
  divPrincipal.style.margin = '0px';
  divPrincipal.style.transition = '0.5s';
}


